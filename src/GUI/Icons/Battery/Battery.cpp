void Battery::drawBatteryIcon(int x, int y, int batteryStage) {
  // Определение массива в зависимости от заряда АКБ
  const uint16_t (*batteryIcon)[21];
  if (batteryStage >= 90) {
    batteryIcon = BatteryStageFull;
  } else {
    // Другие условия будут добавлены в будущем
    // Пока оставим иконку BatteryStageFull для заряда >= 90%
    batteryIcon = BatteryStageFull;
  }

  // Отрисовка пикселей
  for (int i = 0; i < 11; ++i) {
    for (int j = 0; j < 21; ++j) {
      uint16_t color = batteryIcon[i][j];
      dInstance.getDisplay().drawPixel(x + j, y + i, color);
    }
  }
}